declare(strict_types=1);

namespace bora\command\defaults;

use bora\command\CommandSender;
use bora\lang\TranslationContainer;
use bora\Player;

class SeedCommand extends VanillaCommand{

	public function __construct(string $name){
		parent::__construct(
			$name,
			"%bora.command.seed.description",
			"%commands.seed.usage"
		);
		$this->setPermission("bora.command.seed");
	}

	public function execute(CommandSender $sender, string $commandLabel, array $args){
		if(!$this->testPermission($sender)){
			return true;
		}

		if($sender instanceof Player){
			$seed = $sender->getLevel()->getSeed();
		}else{
			$seed = $sender->getServer()->getDefaultLevel()->getSeed();
		}
		$sender->sendMessage(new TranslationContainer("commands.seed.success", [$seed]));

		return true;
	}
}
